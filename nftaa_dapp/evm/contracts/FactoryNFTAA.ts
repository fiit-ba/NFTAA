import { ContractInterface, ethers } from 'ethers'
import { BaseContract } from './BaseContract'
import { NFTAA_FACTORY_ADDRESS } from '~~/evm/constants'
import {Listener} from "@ethersproject/abstract-provider";

export class FactoryNFTAA extends BaseContract {
  protected readonly contract: ethers.Contract

  constructor() {
    super()

    if (useWalletStore().selectedAddress !== null) {
      this.contract = new ethers.Contract(
        NFTAA_FACTORY_ADDRESS,
        ContractABI,
        this.signer
      )
    } else {
      throw new Error('Chain id can not be null')
    }
  }

  public async safeMint(description: string) {

	  let result = this.handleTransaction(
		  await this.contract.safeMint(description),
		  'Mint completed',
		  'Mint failed'
	  )

    this.contract.on('NewNFTAA', (data: string, event) => {
      console.log(event)
      console.log(data)
    });

		return  result;
  }

  public async transferFrom(from: string, to: string, tokenId: number) {
    return this.handleTransaction(
      await this.contract.transferFrom(from, to, tokenId),
      'Transfer completed',
      'Transfer failed'
    )
  }
}

